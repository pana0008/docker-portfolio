            <summary>How can you print a document from your laptop at HZ?</summary>
            <div>
                <ol>
                    <li>
                        Go to the
                        <a href="https://hz.mynetpay.nl/Login" target="_blank" class="faq-link">HZ website</a> and login
                        with your username and password.
                    </li>
                    <li>
                        Click on Choose File and select the file you want to print - repeating the step if you need to
                        add more documents.
                    </li>
                    <li>
                        After selecting all documents, tick the Advanced option to choose whether you want double-sided
                        or black & white printing.
                    </li>
                    <li>
                        Choose between the "HZ printer"/"HZ plotter". After requesting the print you must register using
                        your HZ pass on the TouchID next to the multifunctional printer and select "Print".
                    </li>
                    <li>
                        In the menu, select the printer to which you requested the print.
                    </li>
                    <li>
                        The print will be completed only if there is enough credit on your printing account.
                    </li>
                    <li>
                        When everything is completed, press "Stop" on the TouchID, and lastly - "Logout".
                    </li>
                </ol>
            </div>
